export default function PrivacyPolicy() {
  const lines = privacyPolicyText.split('\n');
  const content: React.ReactNode[] = [];
  let currentListItems: string[] = [];
  let key = 0;

  const flushList = () => {
    if (currentListItems.length === 0) return;
    content.push(
      <ul key={`list-${key++}`} className="list-disc pl-6 space-y-1">
        {currentListItems.map((item) => (
          <li key={`item-${key++}`}>{item}</li>
        ))}
      </ul>
    );
    currentListItems = [];
  };

  const isSectionHeading = (line: string) =>
    /^\d+\.\s/.test(line) || line === 'SUMMARY OF KEY POINTS' || line === 'TABLE OF CONTENTS';

  for (const rawLine of lines) {
    const line = rawLine.trim();

    if (!line) {
      flushList();
      continue;
    }

    if (line.startsWith('- ')) {
      currentListItems.push(line.slice(2));
      continue;
    }

    flushList();

    if (isSectionHeading(line)) {
      content.push(
        <h3 key={`h3-${key++}`} className="text-lg font-semibold mt-6">
          {line}
        </h3>
      );
      continue;
    }

    if (line.startsWith('Last updated')) {
      content.push(
        <p key={`updated-${key++}`} className="italic">
          {line}
        </p>
      );
      continue;
    }

    content.push(
      <p key={`p-${key++}`} className="leading-7">
        {line}
      </p>
    );
  }

  flushList();

  return (
    <section className="max-w-4xl mx-auto px-4 sm:px-6 lg:px-8 space-y-4 text-gray-300">
      <h2 className="text-2xl font-bold text-white">PRIVACY POLICY</h2>
      <div className="space-y-3">{content}</div>
    </section>
  );
}
